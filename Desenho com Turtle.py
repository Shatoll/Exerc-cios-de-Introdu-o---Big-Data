import turtle

# Desenhando uma estrela no Python Turtle Graphics:
t = turtle.Turtle()
for i in range(5):
    t.forward(4*50)
    t.right(4*144)

# Desenhando uma "círculo inscrito" em vários triângulos:

t = turtle.Turtle()
for i in range(30):
    t.forward(4*50)
    t.right(4*147)

turtle.done()